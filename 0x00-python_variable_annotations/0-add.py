#!/usr/bin/env python3
"""
a python function to find the sum of two numbers
using python annotation
"""


def add(a: float, b: float) -> float:
    """ add function to add & return the sum of two numbers using annotation
    Arguments:
        a: the first number
        b: the second number
    Return:
        the sum of the two numbers
    """
    return (a + b)

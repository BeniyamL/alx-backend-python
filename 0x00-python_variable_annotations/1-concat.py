#!/usr/bin/env python3
"""
a python function to find the concatenation of two strings
using python annotation
"""


def concat(str1: str, str2: str) -> str:
    """ concate function to find the concetenation of two strings
    Arguments:
        str1: the first string
        str2: the second string
    Return:
        the concatenation of the two strings
    """
    return (str1 + str2)
